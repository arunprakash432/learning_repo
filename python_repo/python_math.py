import math as m
# python has a built-in module called math, which extends the list of the mathematical functions.
# to use it, you must import the math module
x = m.sqrt(64)
print(x)

# math.ceil() method rounds a number upwards to its nearest integer.
# math.floor() method rounds a number downwards to its nearest integer.

y = m.ceil(11.8)
z = m.floor(11.8)
print(y, z)

a = m.pi
print(a)

