#Escape strings

print("Akatsuki is the best \'villain group in entire naruto")

# \' single quote
# \\ backslash
print("hello\"hello\"world")
# \n Newline
print("Hello\nWorld")
# \r carriage return
print("Hello \r World")
# \t Tab
print("Hello \t World")
# \b Backspace 
print("Hello \b World")
# \f form feed
print("Hello \f world")
# \ooo octal value
a = "\100\101\102\103"
print(a)
# \xhh hex value
b = "\x48\x49\x50"
print(b)
